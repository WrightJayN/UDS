    static loadMapImage(imagePath, canvas) {
        return new Promise((resolve, reject) => {
            const img = new Image();
            img.onload = () => {
                canvas.width = img.width;
                canvas.height = img.height;
                const context = canvas.getContext('2d');
                context.drawImage(img, 0, 0);
                resolve(img);
            };
            img.onerror = () => reject(new Error('Image not found'));
            img.src = imagePath;
        });
    }

    static getBuildingVertices(buildingNode) {
        const buildingName = buildingNode.name;
        return this.buildingVerticesHashMap.get(buildingName);
    }

    static colorBuilding(vertices, color, context) {
        if (!vertices || !Array.isArray(vertices) || vertices.length === 0 || !color || !context) return false;
        context.fillStyle = color;
        context.beginPath();
        context.moveTo(vertices[0].x, vertices[0].y);
        for (let i = 1; i < vertices.length; i++) context.lineTo(vertices[i].x, vertices[i].y);
        context.closePath();
        context.fill();
        return true;
    }

    static drawPathLine(path, context) {
        if (!path || !Array.isArray(path) || path.length < 2 || !context) return false;
        context.strokeStyle = '#FFD700';
        context.lineWidth = 5;
        context.beginPath();
        context.moveTo(path[0].x_coor, path[0].y_coor);
        for (let i = 1; i < path.length; i++) context.lineTo(path[i].x_coor, path[i].y_coor);
        context.stroke();
        return true;
    }

    static drawMapKey(context, canvasWidth, canvasHeight, scale = 2.5) {
        const baseKeyWidth = 200, baseKeyHeight = 90, basePadding = 10;
        const baseSymbolSize = 20, baseLineLength = 50, baseFontSize = 12, baseSpacing = 30;
        const keyWidth = baseKeyWidth * scale, keyHeight = baseKeyHeight * scale;
        const padding = basePadding * scale, symbolSize = baseSymbolSize * scale;
        const lineLength = baseLineLength * scale, fontSize = baseFontSize * scale, spacing = baseSpacing * scale;
        const marginX = 20 * scale, marginY = 20 * scale;
        const keyX = canvasWidth - keyWidth - marginX, keyY = canvasHeight - keyHeight - marginY;
        context.fillStyle = '#FFFFFF';
        context.fillRect(keyX - padding, keyY - padding, keyWidth + 2*padding, keyHeight + 2*padding);
        context.strokeStyle = '#CCCCCC'; context.lineWidth = 2 * scale;
        context.strokeRect(keyX - padding, keyY - padding, keyWidth + 2*padding, keyHeight + 2*padding);
        context.font = `${fontSize}px Arial`; context.fillStyle = '#000000'; context.textBaseline = 'middle';
        const textOffsetX = symbolSize + 8 * scale, startY = keyY + padding + symbolSize / 2;
        context.fillStyle = '#FF0000'; context.fillRect(keyX+padding, keyY+padding, symbolSize, symbolSize);
        context.fillStyle = '#000000'; context.fillText("Source Building", keyX+padding+textOffsetX, startY);
        context.fillStyle = '#0000FF'; context.fillRect(keyX+padding, keyY+padding+spacing, symbolSize, symbolSize);
        context.fillStyle = '#000000'; context.fillText("Destination Building", keyX+padding+textOffsetX, startY+spacing);
        const lineY = keyY + padding + 2 * spacing;
        context.strokeStyle = '#FFD700'; context.lineWidth = 5 * scale;
        context.beginPath(); context.moveTo(keyX+padding, lineY+symbolSize/2); context.lineTo(keyX+padding+lineLength, lineY+symbolSize/2); context.stroke();
        context.fillStyle = '#000000'; context.fillText("Path", keyX+padding+lineLength+8*scale, lineY+symbolSize/2);
    }

    static createMapWithPath(sourceBuilding, destinationBuilding, path, canvas) {
        const context = canvas.getContext('2d');
        this.drawPathLine(path, context);
        const sourceVertices = this.getBuildingVertices(sourceBuilding);
        if (sourceVertices) this.colorBuilding(sourceVertices, '#FF0000', context);
        const destinationVertices = this.getBuildingVertices(destinationBuilding);
        if (destinationVertices) this.colorBuilding(destinationVertices, '#0000FF', context);
        this.drawMapKey(context, canvas.width, canvas.height);
        return canvas;
    }
